package chatbotTask;

public class PostOperation {
	
	public String type;
	public String date;
	public String addr;
	public String index;
	public String attr;
	
	public PostOperation(String type, String date, String addr, String index, String attr) {
		this.type = type;
		this.date = date;
		this.addr = addr;
		this.index = index;
		this.attr = attr;
	}
}
